package beerking;


public class BeerBean {

	private int beernum ;
	private String beerEname ;
	private String beerKname ;
	private String typebig ;
	private String typesmall ;
	private double alchol ;
	private String company ;
	private String country ;
	private String filename;
	
	public int getBeernum() {
		return beernum;
	}
	public void setBeernum(int beernum) {
		this.beernum = beernum;
	}
	public String getBeerEname() {
		return beerEname;
	}
	public void setBeerEname(String beerEname) {
		this.beerEname = beerEname;
	}
	public String getBeerKname() {
		return beerKname;
	}
	public void setBeerKname(String beerKname) {
		this.beerKname = beerKname;
	}
	public String getTypebig() {
		return typebig;
	}
	public void setTypebig(String typebig) {
		this.typebig = typebig;
	}
	public String getTypesmall() {
		return typesmall;
	}
	public void setTypesmall(String typesmall) {
		this.typesmall = typesmall;
	}
	public double getAlchol() {
		return alchol;
	}
	public void setAlchol(double alchol) {
		this.alchol = alchol;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getFilename() {
		return filename;
	}
	public void setFilename(String filename) {
		this.filename = filename;
	}
}
